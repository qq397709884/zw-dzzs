package cn.longicorn.modules.utils;

public class User {
	private int userId = 0;
	private String name = "Default";

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
